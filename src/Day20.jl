function printimage(img)
    for row in eachrow(img)
        for c in collect(row)
            print([".", "#"][c + 1])
        end
        println()
    end
    println("\n")
end

function applyalgo(a, algo, x, y)
    return algo[evalpoly(2, [a[x + 1, y + 1], a[x + 1, y], a[x + 1, y - 1], a[x, y + 1],
       a[x, y], a[x, y - 1], a[x - 1, y + 1], a[x - 1, y], a[x - 1, y - 1]]) + 1]
end

function Base.iterate(imi::ImageIter, state=(zeros(Int, 1, 1), 0))
    if last(state) == 0
        state = (imi.init, 0)
    end
    border = first(imi.algo) != 0 && isodd(last(state))
    nextimage = zeros(Int, size(first(state)) .+ 2)
    workimage = zeros(Int, size(first(state)) .+ 4)
    workimage[begin+2:end-2, begin+2:end-2] .= first(state)
    workimage[[1, 2, end - 1, end], :] .= border
    workimage[:, [1, 2, end - 1, end]] .= border
    for c in CartesianIndices(nextimage)
        nextimage[c] = applyalgo(workimage, imi.algo, c[1] + 1, c[2] + 1)
    end

    return nextimage, (nextimage, last(state) + 1)
end

function day20()
    part = [0, 0]
    algo = collect(replace(algo20, r"\s+" => "")) .== '#'
    lines = filter(!isempty, strip.(split(data20, "\n")))
    mat = zeros(Int, length(lines), length(first(lines)))
    for i in 1:length(lines)
        mat[i, :] .= collect(lines[i]) .== '#'
    end
    imageiter = ImageIter(algo, mat)
    for (step, img) in enumerate(imageiter)
        if step == 2
            part[1] = sum(img)
        elseif step == 50
            part[2] = sum(img)
            break
        end
    end
    return part
end

